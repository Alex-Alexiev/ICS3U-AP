/*
 * This program calculates the slope of a line given x1, x2, y1, and y2
 */

package ca.bayviewglen.daythree;

public class HomeworkFour {

	public static void main(String[] args) {
		
		double x1, y1, x2, y2, m;
		
		x1 = 5;
		y1 = 6;
		x2 = 7;
		y2 = 8;
		
		m = (y2-y1)/(x2-x1);
		
		System.out.println(m);

	}

}
