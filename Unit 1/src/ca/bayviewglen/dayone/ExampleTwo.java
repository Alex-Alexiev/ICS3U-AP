package ca.bayviewglen.dayone;

public class ExampleTwo {

	public static void main(String[] args) {
		
		/*
		\" is an escape sequence for "
		\t is an escape sequence for the tab
		\n is an escape sequence for a new line
		\\ is an escape sequence for a tab
		*/
		
		System.out.println("My favourite word is \"nutmeg\"");
		System.out.println("This is a backslash \\");
		System.out.println("This is a new line \nhello there");

	}

}
